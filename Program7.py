# Grading Scheme
marks = int(input("Enter marks:\n"))

if marks < 50 :
    print("Grade F")
elif 50<=marks<60 :
    print("Grade D")
elif 60<=marks<70 :
    print("Grade C")
elif 70<=marks<80 :
    print("Grade B")
elif 80<=marks<90 :
    print("Grade A")
elif 90<=marks<100 :
    print("Grade A+")
else:
    print("Enter Valid Marks!\n")
